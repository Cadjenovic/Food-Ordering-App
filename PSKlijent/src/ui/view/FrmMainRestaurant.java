    private void fillPorudzbine(){
        try{
            List<Porudzbina> porudzbineSve = CommunicationController.getInstance().getAllOrders();
            List<Porudzbina> porudzbine = new ArrayList<>();
            for(Porudzbina p : porudzbineSve){
                if(p.getKorisnikR().getId() == korisnik.getId()){
                    porudzbine.add(p);
                }
            }
            TableModelPorudzbinaRestoran model = new TableModelPorudzbinaRestoran(porudzbine);
            tblPorudzbine.setModel(model);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Sistem ne moze da pronadje porudzbine", "Neuspesno", JOptionPane.ERROR_MESSAGE);
        }
    }

