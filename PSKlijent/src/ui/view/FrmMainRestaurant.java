    private void btnDeleteOfferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteOfferActionPerformed
        try{
            TableModelPonuda p = (TableModelPonuda) tblPonuda.getModel();
            Ponuda ponuda = p.getPonudatAt(tblPonuda.getSelectedRow());
            p.removeOffer(tblPonuda.getSelectedRow());
            CommunicationController.getInstance().deleteOffer(ponuda);
            System.out.println(ponuda.getPonudaID());
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Morate izabrati ponudu koju zelite da obrisete!", "Neuspesno brisanje", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteOfferActionPerformed

