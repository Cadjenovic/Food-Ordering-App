    private void btnCancelOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelOrderActionPerformed
        try{
            int dialogOption = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da otkazete porudzbinu?");
            TableModelPorudzbina p = (TableModelPorudzbina) tblPorudzbine.getModel();
            Porudzbina porudzbina = p.getPorudzbinatAt(tblPorudzbine.getSelectedRow());
            if(dialogOption == JOptionPane.YES_OPTION){
                p.removeOrder(tblPorudzbine.getSelectedRow());
                CommunicationController.getInstance().deleteOrder(porudzbina);
                System.out.println(porudzbina.getPorudzbinaID());
                JOptionPane.showMessageDialog(this, "Uspesna otkazana porudzbina!", "Uspesno otkazivanje", JOptionPane.INFORMATION_MESSAGE);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Morate izabrati porudzbinu koju zelite da otkazete!", "Neuspesno otkazivanje", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCancelOrderActionPerformed

