    private void prepareView() {
        System.out.println(korisnik.getNazivKorisnika());
        System.out.println(korisnik.getPasswordKorisnika());
        System.out.println(korisnik.getTipKorisnikaID().getTipKorisnikaNaziv());
        this.setTitle(korisnik.getNazivKorisnika());
        this.setLocationRelativeTo(null);
        this.setTitle("Glavni meni musterija");
        fillPorudzbine();
    }
    
    private void fillPorudzbine(){
        try{
            List<Porudzbina> porudzbineSve = CommunicationController.getInstance().getAllOrders();
            System.out.println(porudzbineSve.size());
            List<Porudzbina> porudzbine = new ArrayList<>();
            for(Porudzbina p : porudzbineSve){
                if(p.getKorisnikC().getId() == korisnik.getId()){
                    porudzbine.add(p);
                }
            }
            TableModelPorudzbina model = new TableModelPorudzbina(porudzbine);
            tblPorudzbine.setModel(model);
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
