    private void btnDodajStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajStavkuActionPerformed
        try {
            TableModelStavkaPonude model = (TableModelStavkaPonude)tblStavke.getModel();
            TableModelArtikl model2 = (TableModelArtikl)tblArtikli.getModel();
            int selectedRow = tblArtikli.getSelectedRow();
            if(selectedRow == -1){
                throw new Exception("Izaberite artikl!");
            }
            if(txtStavkaNaziv.getText().length() < 1){
                throw new Exception("Unesite naziv stavke!");
            }
            if(Double.parseDouble(txtCenaStavke.getText()) <= 0){
                throw new Exception("Cena mora biti veca od 0!");
            }
                 Artikl artikl = model2.getArtiklAt(selectedRow);
                 double cena = Double.parseDouble(txtCenaStavke.getText());
                 String naziv = txtStavkaNaziv.getText();
                 StavkaPonude stavka = new StavkaPonude();
                 stavka.setArtikl(artikl);
                 stavka.setStavkaPonudeNaziv(naziv);
                 stavka.setCena(cena);
                model.addOfferItemNew(stavka);
        } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Unos nove ponude", JOptionPane.ERROR_MESSAGE);
         }
        
    }//GEN-LAST:event_btnDodajStavkuActionPerformed

