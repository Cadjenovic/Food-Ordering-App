    private void btnSacuvajPonuduActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajPonuduActionPerformed
        try{
            TableModelStavkaPonude modelS = (TableModelStavkaPonude) tblStavke.getModel();
            Ponuda p = modelS.getPonuda();
            p.setPonudaNaziv(txtOfferName.getText());
            p.setKorisnikID(korisnik);
            for(StavkaPonude s : p.getStavkePonude()){
                p.setPonudaCena(p.getPonudaCena() + s.getCena());
            }
            p = CommunicationController.getInstance().saveOffer(p);
            model.insertPonuda(p);
            model.fireTableDataChanged();
            JOptionPane.showMessageDialog(this, "Uspesna kreirana ponuda!", "Uspesna ponuda", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage(), "Unos nove ponude", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnSacuvajPonuduActionPerformed

